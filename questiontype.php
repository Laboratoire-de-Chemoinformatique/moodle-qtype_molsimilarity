class qtype_molsimilarity extends question_type {

    public function extra_question_fields() {
        return array('question_molsimilarity', 'stereobool', 'threshold', 'alpha', 'scaffold');
    }
    public function move_files($questionid, $oldcontextid, $newcontextid) {
        parent::move_files($questionid, $oldcontextid, $newcontextid);
        $this->move_files_in_answers($questionid, $oldcontextid, $newcontextid);
        $this->move_files_in_hints($questionid, $oldcontextid, $newcontextid);
    }

    protected function delete_files($questionid, $contextid) {
        parent::delete_files($questionid, $contextid);
        $this->delete_files_in_answers($questionid, $contextid);
        $this->delete_files_in_hints($questionid, $contextid);
    }

    public function save_question_options($question) {

        parent::save_question_options($question);

        $this->save_question_answers($question);

        $this->save_hints($question);
    }

    /**
     * executed at runtime (e.g. in a quiz or preview
     *
     * @param question_definition $question
     * @param $questiondata
     */
    protected function initialise_question_instance(question_definition $question, $questiondata) {
        parent::initialise_question_instance($question, $questiondata);
        $this->initialise_question_answers($question, $questiondata);
    }

    /**
     * @param $questiondata
     * @return null
     */
    public function get_random_guess_score($questiondata) {
        return null;
    }

    public function get_possible_responses($questiondata): array {
        return array();
    }
}
